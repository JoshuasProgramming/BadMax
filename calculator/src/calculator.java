package calculator.src;

public class calculator{

     /**
     * Addition method
     * @param x
     * @param y
     * @return x + y
     */

    public int addition(int x, int y){
        return x + y;
    }

    /**
     * Subtraction method
     * @param x
     * @param y
     * @return x - y
     */

    public int subtraction(int x, int y){
        return x - y;
    }

    /**
     * Multiplication method
     * @param x
     * @param y
     * @return x * y
     */

    public int multiplication(int x, int y){
        return x*y;
    }

    /**
     * Division method
     * @param x
     * @param y
     * @return x / y
     */

    public int division(int x, int y){
        return x/y;
    }

}